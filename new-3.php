            <h3> GIBSON: Announces Chris Cornell Tribute Guitar </h3>
            <br>
            <img src="./imagenes/new-3-1.png">
            <br>
            <p>
            GIBSON announced today, Tuesday, January 15, the release of a limited-edition Chris Cornell Tribute ES-335 
            guitar. The guitar will officially be unveiled at "I Am The Highway: A Tribute To Chris Cornell"
             the star-studded concert event honoring Chris Cornell's legacy and contribution to music history on 
             Wednesday, January 16 at The Forum in Los Angeles. For more information, visit: http://www.chriscornell.com.
             </p>
             <p>
             "Chris Cornell was not only one of the most iconic singer songwriters of all time, he was a modern innovator 
             who left a legacy that transcends music genres and generations," says Cesar Gueikian, Chief Merchant Officer,
              GIBSON. "It is our honor to release this tribute ES-335 in his memory. It's a guitar Chris fell in love with 
              and which became a part of his creative expression. Chris was a special artist and we hope that this guitar 
              will inspire fans to find their sound, and inspiration, just like Chris did, for many years to come."
              </p>
              <p>
              GIBSON partnered with Chris to work on his original ES-335 Artist Model and it was immediately clear that he 
              had an innovative eye for guitar design. Features like the aged Lollartron humbucking pickups, clear "no number" 
              Top Hat knobs, Olive Drab Satin finish, and Mother-of-Pearl Chris Cornell 'signature' peghead inlay make this 
              guitar one of GIBSON's most distinct artist offerings to date. Available in a limited release of only 250 pieces
               available for purchase worldwide in Spring 2019.
               </p>
               <p>
               The "I Am The Highway: A Tribute to Chris Cornell" will feature members of Soundgarden, Temple of the Dog, and 
               Audioslave, as well as, special performances from theFoo Fighters, Metallica, Ryan Adams, and more. Jimmy Kimmel 
               will be on hand to host the concert. Tickets will benefit The Chris and Vicky Cornell Foundation, and The 
               Epidermolysis Bullosa Medical Research Foundation, a nonprofit dedicated to raising funds and awareness for 
               this painful and debilitating genetic disorder. GIBSON will donate two of the limited-edition Chris Cornell 
               ES-335 models in partnership with the show to raise additional monies for both foundations.
               </p>
