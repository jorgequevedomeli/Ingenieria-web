            <h2>Gibson Reveals Exciting New Product Line-Up & Artist Collaborations Ahead of Winter NAMM  </h2>
            
            <h4 class="text-center"> NEW GIBSON GENERATION LINE INCLUDING THE G-45 STUDIO AND G-45 STANDARD ANNOUNCED 
            TODAY. CHUCK BERRY ES 350T AND CHRIS CORNELL ES 335 TRIBUTES CONFIRMED FOR 2019 RELEASE </h4>
            <p>
            NASHVILLE, Tenn., Jan. 18, 2019 -- Winter NAMM Room #207 -- As previously announced, in 2019, after more than a century, 
            GIBSON artists, players and fans will experience the all American-made instrument brand rejuvenated. The guitar of 
            choice for artists in rock, pop, country, blues, folk, jazz and beyond, the new GIBSON era celebrates iconic models 
            while looking to the future with instruments that will nurture new players across generations, genders and genres of
             music. GIBSON will showcase the new line-up and artist collaborations next week at NAMM in Anaheim, CA, January 
             23-27. The new GIBSON guitars will be available for purchase later this year.
            </p>
            <div class="row">
                <div class="col s4">
                    <img src="./imagenes/new-2-1.jpg" class="imagennew">
                </div>
                <div class="col s4">
                    <img src="./imagenes/new-2-2.jpg" class="imagennew">
                </div>
                <div class="col s4">
                    <img src="./imagenes/new-2-3.jpg" class="imagennew">
                </div>
            </div>
            <p>
            "We're paying tribute to the Golden Era of Gibson by restoring authenticity for musicians with our new lineup of Classics," 
            says Cesar Gueikian, GIBSON's Chief Merchant Officer. "True to our DNA, our new Les Paul Standard returns to the classic 
            design that made it famous. And the same can be said of other models" adds Gueikian. Designs like the Les Paul and the 
            SG once again embrace the features and construction details that made them legends in the first place: a Les Paul Standard
            '50s spec and a Les Paul Standard '60s spec as well as one with P90 pickups; a Les Paul Special in TV Yellow and Les Paul 
            Junior Tobacco Burst; an SG Standard and two new 61 spec SG's with "maestro" and "sideways" Vibrolas.
            </p>
            <p>
            "We're refocusing on our core range of guitars that will become the foundation for our future," Gueikian says. "And that will 
            give us the ability to bring new ideas to market with limited runs.
            </p>
            <p>
            The new Contemporary lineup includes a Les Paul Tribute Double Cut Junior with a single P90 and a Les Paul Tribute Double Cut Special 
            with dual P90's, both offer fans an accessible price point with the quality and craftmanship of a GIBSON guitar made by our luthiers in Nashville,
             TN. "The Contemporary line also introduces a new concept with the Les Paul and SG Modern," adds Gueikian. "We did a listening tour visiting with dealers,
              artists and guitar enthusiasts and took the best ideas into the new Modern offerings. They incorporate many contemporary updates that 
              players have embraced such as lighter-weight bodies, innovative slim taper neck profiles for full-access to the high frets, ebony fingerboards,
               high output versatile pickups and a push-pull control system to pivot between the humbucker and the P90 sounds, both of which have defined so many genres
                of music since we invented them."
            </p>
            <p>
                "This refocusing extends well beyond GIBSON's electric guitar designs onto our acoustic designs," adds Gueikian. For starters, 
                there is the new Gibson Generation line of acoustics--high-quality, USA-made guitars built by the same hands that make
                the J-200 and J-45's by providing premium access aimed to younger, or newer, "generations" of players. This new 
                Gibson Generation line's first offerings, the G-45 Studio and G-45 Standard, priced at $999 and $1299 respectively,
                feature solid Sitka spruce tops and walnut back and sides and exquisite finishes. "We are very excited about the 
                new Gibson Generation line offering the Gibson craftmanship at an accessible premium price point."
            </p>
            <p>
            At the pinnacle of Gibson, is the Gibson Custom Shop which is currently making the most historically-accurate vintage 
            re-issues ever made. "We have recently put our Custom Shop re-issues in the hands of some of the most knowledgeable 
            vintage experts and collectors, who inspected every detail and put them through the lens of the 'ultimate guitar geek
             test' and they were blown away by our craftmanship, accuracy, aging techniques and sound," says Gueikian. "And 
             then you have our made to measure custom program, where our artists and fans can design their own guitar, which 
             today represents a large and growing share of our Custom Shop production. That's a testament to the quality,
              craftmanship and innovation being driven out of the Custom Shop."
            </p>
            <p>
            GIBSON teamed with Chuck Berry's Son and Grandson to create Berry's famed ES-350T which will be out this year. GIBSON 
            will also release a limited-edition, Chris Cornell Tribute Model ES-335; only 250 will be available with a portion 
            going to charities in his name. Over the years, GIBSON partnered with Chris to work on his original ES-335 Artist 
            Model and it was immediately clear that he had an innovative eye for guitar design. Features like the aged Lollartron
             humbucking pickups, clear "no number" Top Hat knobs, Olive Drab Satin finish, and Mother-of-Pearl Chris Cornell 
             'signature' peghead inlay make this guitar one of GIBSON's most distinct artist offerings to date and more 
             importantly a fitting tribute to Chris Cornell.
            </p>
