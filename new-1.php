            <h1> GIBSON: LAUNCHES NEW PRODUCT, SHOWCASES MARQUEE ARTISTS AND NEW TALENT WORLDWIDE AT NAMM 2019 </h1>
            <br>
            <br>
            <p>
            (February 1, 2019 Nashville, TN) Writing a new chapter to the guitar of choice for artists in rock, pop, 
            country, blues, folk, jazz, and beyond, GIBSON ignited its next era at NAMM in Anaheim, CA. The pre-eminent 
            American-made instrument brand revitalized, re-envisioned, and recharged its core mission, paying homage to
             its storied history as it embraced the next vanguard of musicians independent of generation, gender and 
             genre. NAMM represented a series of achievements for GIBSON including a NAMM Milestone Award for 125 years 
             in business and the new Les Paul Standard 50s guitar went on to win "Best Electric Guitar of NAMM" from the 
             editors of Guitar, Total Guitar and Music Radar magazines.
            </p>
            <p>
            Attracting widespread critical praise, the GIBSON Experience at NAMM brought in a stunning array of new and 
            old guitars in a relaxed atmosphere. Players could pick up their guitar of choice in the GIBSON room and plug 
            in, put on headphones and sit down to play on their own terms.
            </p>
            <img src="./imagenes/new-1-1.jpg">
            <br>
            <img src="./imagenes/new-1-2.jpg">
            <br>

           
            <p>
            On Wednesday, January 23, GIBSON commenced the week by launching the Chuck Berry Tribute ES-350T which marks the 
            first-ever collaboration with the rock n' roll pioneer's family. Chuck Berry's son Charles Jr. and grandson Charlie 
            took the GIBSON stage alongside Jared James Nichols and introduced the instrument and delivered a powerful performance
             of "Johnny B. Goode" that thrilled the packed crowd and set the fast-paced tone for the entire festival. Music Radar
              claimed, "GIBSON has already been making a splash on its return to the NAMM fray, and it did its best to dominate 
              the proceedings at the pre-show event." Regarding the new model, Guitar wrote, "The collection had gorgeous guitars
               galore, but one of the standouts had to be Custom Shop Chuck Berry Tribute ES-350T."
            </p>
            <br>
            <img src="./imagenes/new-1-3.jpg">
            <br>
            <p>
            For Thursday, GIBSON's opening party maintained its momentum well into the night by hosting the week's hottest nighttime event, 
            the NAMM JAM "Legends and New Icons" at City National Grove of Anaheim. The opening party saw a bevy of legends share 
            the stage, including none other than Billy Gibbons, Nancy Wilson, Peter Frampton, Robby Krieger, Emily Wolfe, Black 
            Pistol Fire, Cam, Jared James Nichols , Toby Lee, Lauren Ruth Ward and more. Meanwhile, the "house band" 
            comprisedJimmy Vivino, Kenny Aronoff, Darryl Jones, and Jeff Young.
            </p>
            <p>
            Thursday through Sunday saw the GIBSON Experience welcome a diverse range of artists to the second floor in Room 207 for signings, 
            Q&A's, appearances and special live performances. These events comprised a roster of legends, icons, and future stars includingNancy 
            Wilson of Heart,Robby Krieger of The Doors,Rick Nielsen of Cheap Trick, Lee Roy Parnell, Jason Hook of Five Finger Death Punch, Brian 
            Ray (Paul McCartney), Brendon Small of Metalocalypse andDethklok, Chris Traynor of Bush and Helmet, Billy Howerdell ofA Perfect Circle, 
            Michael Clifford of 5 Seconds of Summer, Lari Basilio,Vivian Campell of Def Leppard,Cam, Emily Wolfe,Frank Hannon, Toby Lee,Striking Matches,
             Luis Maldonado of Train,Trini Lopez, Michelle Arseneau, Gina Chavez,GianMarco, Austin Sexton,Brooklyn Allman, Spindrift, Leo James Conroy, Monica 
             Valli, Carly Jo Jackson and more.
             </p>

